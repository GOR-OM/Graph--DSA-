 bool canFinish(int n, vector<vector<int>>& arr) {
    int m = arr.size();
    vector<int>intake(n,0);
    vector<int>g[n];
    for(int i=0;i<m;i++){
        g[arr[i][0]].push_back(arr[i][1]);
        intake[arr[i][1]]++;
    }
    queue<int>q;
    for(int i=0;i<n;i++){
        if(intake[i]==0){
            q.push(i);
        }
    }
    int vis=0;
    while(!q.empty()){
        int p = q.front();
        q.pop();
        vis++;
        for(int chi : g[p]){
            intake[chi]--;
            if(intake[chi]==0){
                q.push(chi);
            }
        }
    }
    return vis!=n;
        
}



